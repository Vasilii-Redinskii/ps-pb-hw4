def make_low(string):
    """Функция преобразует все символы из строки string в нижний регистр
    :param string: строка, которую нужно преобразовать в нижний регистр"""

    return string.lower()


def make_up(string):
    """Функция преобразует все символы из строки string в верхний регистр
    :param string: строка, которую нужно преобразовать в верхний регистр"""

    return string.upper()
    